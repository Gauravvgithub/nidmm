    const previous = document.querySelector('.previous-1');
    const next = document.querySelector('.next-1');
    const images = document.querySelector('.slider-carousel-1').children;
    const totalImages = images.length;
    let currentIndex = 0;


    // Event Listeners to previous and next buttons
    previous.addEventListener('click', () => {
      previousImage()
    })

    next.addEventListener('click', () => {
      nextImage();
    })

    setInterval(() => {
      nextImage();
    }, 1000);

    // Function to go to next Image
    function nextImage() {

      images[currentIndex].classList.remove('main-1');
      if (currentIndex == totalImages - 1) {
        currentIndex = 0;
      } else {
        currentIndex++;
      }

      images[currentIndex].classList.add('main-1');

    }

    // Function to go to previous Image
    function previousImage() {

      images[currentIndex].classList.remove('main-1');
      if (currentIndex == 0) {
        currentIndex = totalImages - 1;
      } else {
        currentIndex--;
      }

      images[currentIndex].classList.add('main-1');

    }
